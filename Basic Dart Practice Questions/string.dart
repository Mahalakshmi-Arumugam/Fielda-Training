//Write a program to print Hello I am “John Doe” and Hello I’am “John Doe” with single and double quotes.

void main() {
  String input1 = 'Hello I am “John Doe”';
  String input2 = "Hello I am \“John Doe\”";
  print(input1);
  print(input2);

  String input3 = "Hello I’am \“John Doe\”";
  String input4 = 'Hello I’am “John Doe”';
  print(input3);
  print(input4);
}
